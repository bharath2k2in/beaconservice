package com.beacon.model;

public class UserToken {
	private int tokenId;
	private int tokenNumber;
	public int getId() {
		return tokenId;
	}
	public void setId(int id) {
		this.tokenId = id;
	}
	public int getTokenNumber() {
		return tokenNumber;
	}
	public void setTokenNumber(int tokenNumber) {
		this.tokenNumber = tokenNumber;
	}
	
	

}
